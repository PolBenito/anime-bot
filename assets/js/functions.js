function ComprobarSiEstaLogeado() {
    // Miramos si existe el usuario en la BBDD
    return new Promise((resolve) => {
        if (!checkCookie('animebot_user')) resolve(false);
        
        let cookie = JSON.parse(getCookie('animebot_user'));
        var codUserCookie = cookie['COD_USER'];
        var cipherPasswordCookie = cookie['PASSWORD'];

        fetch(pathApi+new URLSearchParams({
            'OP': 'LOGIN',
            'COD_USER': codUserCookie
        }))
        .then(response => response.json())
        .then(data => {
            if (data.estado == "ok") {
                let key = data.res.KEY;
    
                var decryptedPassword = sjcl.decrypt(key, data.res.PASSWORD);
                var decryptedPasswordCookie = sjcl.decrypt(key, cipherPasswordCookie);
                
                if (decryptedPasswordCookie == decryptedPassword) resolve(true);
                else resolve(false);
            }
            else if (data.estado == "ko") resolve(false);
        })                
        .catch(error => {
            console.error(error);
            resolve(false);
        });
    });
}

